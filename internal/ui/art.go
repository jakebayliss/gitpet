func GetArt(species string) string {
	return GetArtStage(species, 1)
}

func GetArtStage(species string, stage int) string {
	key := fmt.Sprintf("%s:%d", species, stage)
	if art, ok := Art[key]; ok {
		return art
	}
	// Fall back to stage 1
	if art, ok := Art[species+":1"]; ok {
		return art
	}
	return `
     ???
    (o.o)
     ~~~`
}
